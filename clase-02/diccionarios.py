# diccionarios

# Crear un diccionario
mi_diccionario = {
    'nombre': 'Juan',
    'edad': 30,
    'ciudad': 'Madrid'
}

# Acceder a un valor usando una clave
print(mi_diccionario['nombre'])  # Imprimirá 'Juan'

# Agregar un nuevo par clave-valor
mi_diccionario['profesión'] = 'Ingeniero'

# Iterar a través de las claves y valores
for clave, valor in mi_diccionario.items():
    print(clave, valor)


